int	ft_atoi(char *str)
{
	int	i;
	int	retu;
	int	s;

	i = 0;
	retu = 0;
	s = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] != '\0' && (str[i] == '+' || str[i] == '-'))
	{
		if (str[i] == '-')
			s++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		retu = (retu * 10) + str[i] - 48;
		i++;
	}
	if (s % 2 != 0)
		retu *= -1;
	return (retu);
}
