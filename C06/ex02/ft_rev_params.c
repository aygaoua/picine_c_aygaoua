#include <unistd.h>

void	pchar(char c)
{
	write(1, &c, 1);
}

int	main(int ac, char	**av)
{
	int	j;
	int	i;

	i = ac - 1;
	while (i > 0)
	{
		j = 0;
		while (av[i][j] != '\0')
		{
			pchar(av[i][j]);
			j++;
		}
		pchar('\n');
		i--;
	}
	return (0);
}
