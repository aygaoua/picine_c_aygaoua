int	ft_sqrt(int nb)
{
	long	ind;
	long	i;

	i = nb;
	if (i <= 0)
		return (0);
	if (i == 1)
		return (1);
	ind = 2;
	if (i >= 2)
	{
		while (ind * ind <= i)
		{
			if (ind * ind == i)
				return (ind);
			ind++;
		}
	}
	return (0);
}
