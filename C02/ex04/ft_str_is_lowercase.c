int	ft_str_is_lowercase(char *str)
{
	int	i;

	if (str == '\0')
		return (1);
	else
	{
		i = 0;
		while (str[i] != '\0')
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				i++;
			else
				return (0);
		}
	}
	return (1);
}
