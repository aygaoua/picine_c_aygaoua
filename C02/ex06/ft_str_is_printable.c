int	ft_str_is_printable(char *str)
{
	int	i;

	if (str == '\0')
		return (1);
	else
	{
		i = 0;
		while (str[i] != '\0')
		{
			if (str[i] >= 32 && str[i] <= 127)
				i++;
			else
				return (0);
		}
		return (1);
	}
}
