void	ft_rev_int_tab(int *tab, int size)

{
	int	i;
	int	khwa;

	i = 0;
	while (i <= (size - 1) / 2)
	{
		khwa = tab[i];
		tab[i] = tab[(size - 1) - i];
		tab[(size - 1) - i] = khwa;
		i++;
	}
}
